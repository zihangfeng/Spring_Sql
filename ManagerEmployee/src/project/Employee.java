package project;

public class Employee {
	private int eid;
	private String name;
	private int salary;
	
	
	public void setEid(int eid)
	{
		this.eid = eid;
	}
	
	public int getEid()
	{
		return eid;
	}
	
	public void setEmployeeName(String name)
	{
		this.name = name;
	}
	
	public String getEmployeeName()
	{
		return name;
	}
	
	public void setSalary(int salary)
	{
		this.salary = salary;
	}
	
	public int getSalary()
	{
		return salary;
	}
	
}
